type FlagComponent = typeof BR;

// Maps FIFA 3-letter team codes (as stored in `teams.code`) to a flag SVG
// component. Most FIFA codes correspond directly to ISO 3166-1 alpha-2; the
// exceptions are FIFA-specific codes (KSA, RSA, GER, ...) and the GB
// subdivisions for England and Scotland.
const FIFA_TO_FLAG: Record<string, FlagComponent> = {
  ALG: DZ,
  ARG: AR,
  AUS: AU,
  AUT: AT,
  BEL: BE,
  BIH: BA,
  BRA: BR,
  CAN: CA,
  CIV: CI,
  COD: CD,
  COL: CO,
  CPV: CV,
  CRO: HR,
  CUW: CW,
  CZE: CZ,
  ECU: EC,
  EGY: EG,
  ENG: GB_ENG,
  ESP: ES,
  FRA: FR,
  GER: DE,
  GHA: GH,
  HAI: HT,
  IRN: IR,
  IRQ: IQ,
  JOR: JO,
  JPN: JP,
  KOR: KR,
  KSA: SA,
  MAR: MA,
  MEX: MX,
  NED: NL,
  NOR: NO,
  NZL: NZ,
  PAN: PA,
  PAR: PY,
  POR: PT,
  QAT: QA,
  RSA: ZA,
  SCO: GB_SCT,
  SEN: SN,
  SUI: CH,
  SWE: SE,
  TUN: TN,
  TUR: TR,
  URU: UY,
  USA: US,
  UZB: UZ,
};

export function getTeamFlag(fifaCode: string): FlagComponent | null {
  return FIFA_TO_FLAG[fifaCode.toUpperCase()] ?? null;
}
